<h2>Enforcement procedure</h2>
<p class="govuk-body">The Equality and Human Rights Commission (EHRC) is responsible for enforcing the Public Sector Bodies (Websites and Mobile Applications) (No. 2) Accessibility Regulations 2018 (the 'accessibility regulations').</p>

<p class="govuk-body">If you're not happy with how we respond to your complaint, <a href="https://www.equalityadvisoryservice.com" target="_New">contact the Equality Advisory and Support Service</a> (opens in a new tab).</p>

<h2>Technical information about this website’s accessibility</h2>
<p class="govuk-body">The Ministry of Defence is committed to making its websites accessible, in accordance with the Public Sector Bodies (Websites and Mobile Applications) (No. 2) Accessibility Regulations 2018.</p>
<p class="govuk-body">This website is fully compliant with the Web Content Accessibility Guidelines version 2.1 AA standard. <a href="https://www.w3.org/TR/WCAG21/" target="_New">Web Content Accessibility Guidelines version 2.1</a> AA standard (opens in a new tab).


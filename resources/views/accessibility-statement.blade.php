                <h1 class="govuk-heading-xl">Accessibility statement for Apply for Armed Forces Compensation or a War Pension service</h1>
<p class="govuk-body">This accessibility statement applies to <a href="https://apply-armed-forces-compensation-or-war-pension.service.gov.uk" target="_New">apply-armed-forces-compensation-or-war-pension.service.gov.uk</a> (opens in a new tab).</p>

